%Gaussova integracija
%u xi su pozitivne nultocke Legendreovih polinoma (integracijske tocke za N=2,3,4,5,6 iz tablice)
function [I,N]=Gaussova_int(a,b,N)
   xi=[0.577350269189626, 0, 0, 0, 0;
       0, 0.774596669241483, 0, 0, 0;
       0.339981043584856, 0.861136311594053, 0, 0, 0;
       0, 0.538469310105683, 0.906179845938664, 0, 0;
       0.238619186083197, 0.661209386466265, 0.932469514203152, 0, 0];
   wi=[1, 0, 0, 0, 0;
       0.888888888888888, 0.555555555555556, 0, 0, 0;
       0.652145154862546, 0.347854845137454, 0, 0, 0;
       0.568888888888889, 0.478628670599366, 0, 0, 0;
       0.467913934572691, 0.360761573048139, 0.171324492379170, 0, 0];
   h=(b-a)/2;
   m=(a+b)/2;
   x=h*xi(N,1);

   if (mod(N,2)~=0)
     Ans=h*wi(N,1)*f(x);
   else
     Ans=h*wi(N,1)*(f(-x+m)+f(x+m));
   end

   for i=2:((N+1)/2)
     x=h*xi(N,i);
     Ans=Ans+h*wi(N,i)*(f(-x+m)+f(x+m));
   end

   I=2/sqrt(pi)*Ans;

end
